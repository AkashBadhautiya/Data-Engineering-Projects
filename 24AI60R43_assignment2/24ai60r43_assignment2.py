#heapify fuction from leaves to toward root
def heapifyup(arr, index):

  parent=(index-1)//2

  while index>0 and arr[index][1]>arr[parent][1]:
    arr[index], arr[parent]=arr[parent], arr[index]
    index=parent
    parent=(index-1)//2

