#implementing heap sort using heapify func
def heap__sort(arr):
  n=len(arr)
  for i in range(n-1, 0, -1):
    arr[i], arr[0]=arr[0], arr[i]
    heapify(arr, i, 0)

  return arr[::-1] #for traversing the  list from end to start index

