# heapify fuction from root to leaves
def heapify(arr, n, i):
  largest=i #lasgest as root
  l=2*i+1 #left child
  r=2*i+2 #right child


  if l<n and arr[largest][1]<arr[l][1]:
    largest=l
  if r<n and arr[largest][1]<arr[r][1]:
    largest=r

  if largest!=i:
    arr[i], arr[largest]=arr[largest], arr[i]
    heapify(arr, n, largest)

#fuction for build heap

def build_heap(arr):
  n=len(arr)
  for i in range(n//2-1, -1, -1):
    heapify(arr, n, i)
  return arr

